namespace Image.Tga
{
    /// <summary>
    /// The possible TGA image formats.
    /// </summary>
    public enum TgaFormat
    {
        /// <summary>
        /// ARBG, 4 bits per component.
        /// </summary>
        Argb4,
        /// <summary>
        /// 5 red, 6 green, 5 blue bits.
        /// </summary>
        Rgb565,
    }
}
